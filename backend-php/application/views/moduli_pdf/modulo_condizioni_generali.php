<h2 style="text-align: center;">CONDIZIONI GENERALI</h2>
<p style="font-size: 7pt">
1) Versamento quota.<br>
II cliente si impegna a corrispondere per intero la quota d'adesione stabilita nel contratto, secondo le modalità ivi indicate prefissate, anche nel caso in cui dovesse rimanere assente e interrompere temporaneamente la frequentazione per qualsiasi motivo non dipendente dal Centro sportivo, senza poter addurre alcuna giustificazione che ne esenti il pagamento o comporti richieste di rimborsi o riduzioni di sorta la <strong><?=$nome_palestra?></strong> potrà a suo insindacabile giudizio concedere eccezionalmente recuperi in caso di assenza forzata, fino ad un massimo di tre mesi. La quota d'iscrizione non è comunque restituibile in nessun caso. né per intero, né per frazioni.<br>
2) Risoluzione unilaterale del contratto.<br>
La <strong><?=$nome_palestra?></strong> riserva a se il diritto di risolvere unirateralmente il rapporto con il Cliente escludendolo senza preavviso dalla prosecuzione del corso. La <strong><?=$nome_palestra?></strong> avrà solo l'obbligo di comunicazione scritta senza necessità di motivazione<br>
3) Custodia valori<br>
La <strong><?=$nome_palestra?></strong> non gestisce servizio di custodia di beni o valori e pertanto non risponde della sottrazione, perdita o deterioramento di qualsiasi oggetto portato dai clienti nei locali del Centro Sportivo neppure se custodito nell'apposito armadietto.<br>
4) Certificato medico.<br>
Per l'accesso e la frequenza alle attività ed ai servizi della <strong><?=$nome_palestra?></strong> cosi come per la partecipazione a qualunque corso, e indispensabile il benestare medico. La visita medica, effettuata nei locali interni della <strong><?=$nome_palestra?></strong>, con relativo benestare rilasciato da qualunque medico in detti locali o anche al di fuori di essi, non implica comunque assunzione di alcuna responsabilità da parte della <strong><?=$nome_palestra?></strong>. Il Cliente frequentante deve rispettare indicazioni tecniche dategli all'atto della visita Medica preventiva e riportate poi nel suo programma personale di attività fisica. Per questo deve rivolgersi al personale tecnico di assisteza per essere correttamente seguito nel suo programma personale e nel corso.<br>
5) Danni alla struttura.<br>
Tutti i danni alle strutture o alla <strong><?=$nome_palestra?></strong>. anche se nell'esercizio di attività svolte con istruttori saranno a carico di chi li avrà cagionati.<br>
6) Assicurazione<br>
La <strong><?=$nome_palestra?></strong> è coperta da assicurazione per la responsabilità civile. Tale assicurazione copre esclusivamente gli eventuali danni a cose o persone causati direttamente per colpa della <strong><?=$nome_palestra?></strong>.<br>
7) Norme generali<br>
Con  la firma posta alle presenti "Condizioni Generali" il Cliente approva espressamente il sotto riferito "regolamento  impegnandosi a rispettarlo e prendendo atto che una sua mancata osservazione può comportare il diritto per la <strong><?=$nome_palestra?></strong> di recedere il contratto dietro comunicazione Scritta motivata e senza alcun onere di risarcimento o rimborso della quota<br>
a) è obbligatorio. per accedere alla palestra, uniforme adeguata (tuta da ginnastica o body calzamaglia, Scarpe non utilizzate all’esterno) 	<br>
b) è obbligatorio lasciare libero l'armadietto e le cassettiere dopo l'uso, in caso contrario la direzione sarà costretta a tagliare il lucchetto;<br>
c) e assolutamente vietato fumare nei locali della <strong><?=$nome_palestra?></strong><br>
<br>
8) adeguamento corsi e struttura<br>
La <strong><?=$nome_palestra?></strong> ha ampia facoltà di modificare a suo piacimento le strutture o le suddivisioni interne oltre che sostituire le attrezzature o i macchinari• usati nei diversi settori. Questo per ottenere un costante adeguamento della <strong><?=$nome_palestra?></strong> stesso ai suoi Fini istituzionali ed alla generale evoluzione tecnologica Il consiglio direttivo può inoltre modificare liberamente gli orari di apertura o di chiusura della <strong><?=$nome_palestra?></strong> stesso, sposare gli orari dei corsi e ridurre le ore dedicate a ciacun corso e ciò senza alcun onore verso il Cliente<br>
9) Trasferimento attività.<br>
Per  motivi di forza maggiore e non dipendenti dalle volontà della <strong><?=$nome_palestra?></strong>, lo stesso può trasferire l'attività c/o cessarla nel caso di cui sopra nulla è dovuto per intero o in parte della quota versata da Cliente alla <strong><?=$nome_palestra?></strong>. Letto, approvato e sottoscritto. Per i minori occorre la firma di un genitore o di chi ne fa le veci.<br>
